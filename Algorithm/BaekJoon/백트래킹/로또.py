import sys
from itertools import combinations
sys.stdin = open("로또.txt", "r")


while True:
    temp = list(map(int, input().split()))
    n = int(temp[0])
    if n == 0:
        break
    for case in combinations(temp[1:], 6):
        print(' '.join(map(str, case)))
    print('')


